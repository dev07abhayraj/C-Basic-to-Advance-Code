#include <stdio.h>

int main(){
    unsigned int num = 5; // num is a variable and signed int is a data type  and we have to mention it before declaring each variable
    printf("%d", num); // "%d" is a format specifier used to print value according to data type
    return 0;
}


// Output : 5

// all the numbers from -345605656 to 345605656 comes under signed integer or int data type
// NOTE : 345605656 this is just a random number
// unsigned integer means a number which have only positive value and zero and doesn't require any sign like -5.

